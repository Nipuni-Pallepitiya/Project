package com.example.finalone.Model;

public class FixedPackages {

    private String fixid;
    private String fixname;
    private String fixanytime;
    private String fixnighttime;
    private String fixvoice;
    private String fixsms;
    private String fixvalidity;
    private String fixprice;

    public FixedPackages() {
    }

    public String getFixid() {
        return fixid;
    }

    public void setFixid(String fixid) {
        this.fixid = fixid;
    }

    public String getFixname() {
        return fixname;
    }

    public void setFixname(String fixname) {
        this.fixname = fixname;
    }

    public String getFixanytime() {
        return fixanytime;
    }

    public void setFixanytime(String fixanytime) {
        this.fixanytime = fixanytime;
    }

    public String getFixnighttime() {
        return fixnighttime;
    }

    public void setFixnighttime(String fixnighttime) {
        this.fixnighttime = fixnighttime;
    }

    public String getFixvoice() {
        return fixvoice;
    }

    public void setFixvoice(String fixvoice) {
        this.fixvoice = fixvoice;
    }

    public String getFixsms() {
        return fixsms;
    }

    public void setFixsms(String fixsms) {
        this.fixsms = fixsms;
    }

    public String getFixvalidity() {
        return fixvalidity;
    }

    public void setFixvalidity(String fixvalidity) {
        this.fixvalidity = fixvalidity;
    }

    public String getFixprice() {
        return fixprice;
    }

    public void setFixprice(String fixprice) {
        this.fixprice = fixprice;
    }
}
