package com.example.finalone.Model;

public class DataPackages {

    private String dataid;
    private String dataname;
    private String dataanytime;
    private String datanighttime;
    private String datavalidity;
    private String dataprice;

    public DataPackages() {
    }

    public String getDataid() {
        return dataid;
    }

    public void setDataid(String dataid) {
        this.dataid = dataid;
    }

    public String getDataname() {
        return dataname;
    }

    public void setDataname(String dataname) {
        this.dataname = dataname;
    }

    public String getDataanytime() {
        return dataanytime;
    }

    public void setDataanytime(String dataanytime) {
        this.dataanytime = dataanytime;
    }

    public String getDatanighttime() {
        return datanighttime;
    }

    public void setDatanighttime(String datanighttime) {
        this.datanighttime = datanighttime;
    }

    public String getDatavalidity() {
        return datavalidity;
    }

    public void setDatavalidity(String datavalidity) {
        this.datavalidity = datavalidity;
    }

    public String getDataprice() {
        return dataprice;
    }

    public void setDataprice(String dataprice) {
        this.dataprice = dataprice;
    }
}
