package com.example.finalone.Model;

public class ShareData {

    private String id;
    private String phnFrom;
    private String phnTo;
    private String amt;
    private String date;

    public ShareData() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPhnFrom() {
        return phnFrom;
    }

    public void setPhnFrom(String phnFrom) {
        this.phnFrom = phnFrom;
    }

    public String getPhnTo() {
        return phnTo;
    }

    public void setPhnTo(String phnTo) {
        this.phnTo = phnTo;
    }

    public String getAmt() {
        return amt;
    }

    public void setAmt(String amt) {
        this.amt = amt;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
